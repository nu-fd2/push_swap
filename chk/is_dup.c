#include "../push_swap.h"

bool is_dup(long nb, char *arr)
{
	int i;

	i = 0;
	if (nb > 2147483647 || nb < -2147483648)
		return (1);
	while (arr[i] != 0)
	{
		if (arr[i] == (int)nb)
			return (1);
		i++;
	}
	arr[i] = (int)nb;
	return (0);
}
